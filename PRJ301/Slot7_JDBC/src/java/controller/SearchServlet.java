    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id_raw = request.getParameter("id");
        String did_raw = request.getParameter("did");
        String name = request.getParameter("name");
        String gender_raw = request.getParameter("gender");
        String from_raw = request.getParameter("from");
        String to_raw = request.getParameter("to");
        Boolean gender = Boolean.getBoolean(gender_raw);

        int did = 0;
        Integer id = null;
        DAO d = new DAO();
        List<Department> list1 = d.getDepts();
        List<Employee> list2 = null;
        try {
            if (id_raw != null && !"".equals(id_raw)) {
                id = Integer.getInteger(id_raw);
            }
            did =Integer.parseInt(did_raw);
//            Date from = new SimpleDateFormat("yyyy-MM-dd").parse(from_raw);
//            Date to = new SimpleDateFormat("yyyy-MM-dd").parse(to_raw);
            list2 = d.search(id, did, name, gender);
        } catch (Exception e) {
        }

        request.setAttribute("depts", list1);
        request.setAttribute("emps", list2);
        request.setAttribute("did", did);
        request.getRequestDispatcher("search.jsp").forward(request, response);
    }

